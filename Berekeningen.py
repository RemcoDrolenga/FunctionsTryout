def addition(getal1,getal2):
    return getal1 + getal2

# print("10 + 12 =",addition(10,12))

def subtraction(getal1,getal2):
    return getal1 - getal2

# print("58 - 34 =",subtraction(58,34))

def muliplication(getal1,getal2):
    return getal1 * getal2

# print("6 x 7 =",muliplication(6,7))

def increment(getal1):
    return getal1 + 1

# print("12 + 1 =",increment(12))

def decrement(getal1):
    return getal1 - 1

print("34 - 1 =",decrement(34))